def import_training_libs():
    try:
        from datasets import Dataset
        from transformers import DataCollatorForSeq2Seq
        from trl import SFTConfig, SFTTrainer
        from unsloth import FastLanguageModel
    except ImportError as exc:
        raise SystemExit(
            "Missing training dependencies. On Vast.ai, install first:\n"
            "pip install unsloth trl transformers datasets accelerate bitsandbytes scikit-learn pandas"
        ) from exc
    return FastLanguageModel, Dataset, SFTConfig, SFTTrainer, DataCollatorForSeq2Seq


def to_text_rows(records: list[dict[str, Any]], tokenizer) -> list[dict[str, str]]:
    rows = []
    for record in records:
        rows.append(
            {
                "text": render_chat_text(
                    tokenizer,
                    record["messages"],
                    add_generation_prompt=False,
                )
            }
        )
    return rows


def render_chat_text(tokenizer, messages: list[dict[str, str]], *, add_generation_prompt: bool) -> str:
    """Render chat messages, disabling Qwen3 thinking when the tokenizer supports it."""
    try:
        return tokenizer.apply_chat_template(
            messages,
            tokenize=False,
            add_generation_prompt=add_generation_prompt,
            enable_thinking=False,
        )
    except TypeError:
        return tokenizer.apply_chat_template(
            messages,
            tokenize=False,
            add_generation_prompt=add_generation_prompt,
        )
    except Exception:
        return render_qwen_messages_manually(messages, add_generation_prompt=add_generation_prompt)


def render_qwen_messages_manually(
    messages: list[dict[str, str]],
    *,
    add_generation_prompt: bool,
) -> str:
    parts = []
    for message in messages:
        parts.append(f"<|im_start|>{message['role']}\n{message['content']}<|im_end|>")
    if add_generation_prompt:
        parts.append("<|im_start|>assistant\n")
    return "\n".join(parts)


def build_trainer(
    *,
    model,
    tokenizer,
    train_dataset,
    validation_dataset,
    SFTConfig,
    SFTTrainer,
    DataCollatorForSeq2Seq,
):
    sft_config = make_sft_config(SFTConfig)
    trainer_kwargs = {
        "model": model,
        "train_dataset": train_dataset,
        "eval_dataset": validation_dataset,
        "data_collator": DataCollatorForSeq2Seq(tokenizer=tokenizer),
        "args": sft_config,
    }
    trainer_init_params = inspect.signature(SFTTrainer.__init__).parameters
    if "processing_class" in trainer_init_params:
        trainer_kwargs["processing_class"] = tokenizer
    else:
        trainer_kwargs["tokenizer"] = tokenizer
    if "dataset_text_field" in trainer_init_params:
        trainer_kwargs["dataset_text_field"] = "text"
    if "max_seq_length" in trainer_init_params:
        trainer_kwargs["max_seq_length"] = MAX_SEQ_LENGTH
    if "packing" in trainer_init_params:
        trainer_kwargs["packing"] = False
    return SFTTrainer(**trainer_kwargs)


def make_sft_config(SFTConfig):
    import torch

    use_bf16 = torch.cuda.is_available() and torch.cuda.is_bf16_supported()
    kwargs = {
        "per_device_train_batch_size": PER_DEVICE_TRAIN_BATCH_SIZE,
        "per_device_eval_batch_size": PER_DEVICE_EVAL_BATCH_SIZE,
        "gradient_accumulation_steps": GRADIENT_ACCUMULATION_STEPS,
        "warmup_steps": WARMUP_STEPS,
        "num_train_epochs": NUM_TRAIN_EPOCHS,
        "learning_rate": LEARNING_RATE,
        "fp16": not use_bf16,
        "bf16": use_bf16,
        "logging_steps": LOGGING_STEPS,
        "optim": OPTIM,
        "weight_decay": WEIGHT_DECAY,
        "lr_scheduler_type": LR_SCHEDULER_TYPE,
        "seed": SEED,
        "output_dir": (OUTPUT_DIR / "checkpoints").as_posix(),
        "report_to": "none",
        "eval_steps": EVAL_STEPS,
        "save_strategy": "steps",
        "save_steps": SAVE_STEPS,
        "load_best_model_at_end": True,
        "metric_for_best_model": "eval_loss",
    }
    params = inspect.signature(SFTConfig.__init__).parameters
    if MAX_STEPS is not None:
        kwargs["max_steps"] = MAX_STEPS
    if "eval_strategy" in params:
        kwargs["eval_strategy"] = "steps"
    elif "evaluation_strategy" in params:
        kwargs["evaluation_strategy"] = "steps"
    else:
        kwargs["eval_strategy"] = "steps"
    if "dataset_text_field" in params:
        kwargs["dataset_text_field"] = "text"
    if "packing" in params:
        kwargs["packing"] = False
    if "max_seq_length" in params:
        kwargs["max_seq_length"] = MAX_SEQ_LENGTH
    elif "max_length" in params:
        kwargs["max_length"] = MAX_SEQ_LENGTH

    if any(param.kind == inspect.Parameter.VAR_KEYWORD for param in params.values()):
        return SFTConfig(**kwargs)
    filtered = {key: value for key, value in kwargs.items() if key in params}
    return SFTConfig(**filtered)


def run_label_score_evaluation(
    *,
    model,
    tokenizer,
    records: list[dict[str, Any]],
    labels: list[str],
) -> list[dict[str, Any]]:
    predictions = []
    for index, record in enumerate(records):
        messages = record["messages"]
        expected = messages[-1]["content"].strip()
        prompt_messages = messages[:-1]
        scores = score_labels(model=model, tokenizer=tokenizer, messages=prompt_messages, labels=labels)
        predicted = max(scores, key=scores.get)
        predictions.append(
            {
                "index": index,
                "expected": expected,
                "predicted": predicted,
                "correct": predicted == expected,
                "scores": scores,
                "text": messages[-2]["content"],
            }
        )
    return predictions


def score_labels(
    *,
    model,
    tokenizer,
    messages: list[dict[str, str]],
    labels: list[str],
) -> dict[str, float]:
    import torch
    import torch.nn.functional as F

    prompt_text = render_chat_text(tokenizer, messages, add_generation_prompt=True)
    prompt_ids = tokenizer(prompt_text, return_tensors="pt", add_special_tokens=False)["input_ids"]
    prompt_length = int(prompt_ids.shape[-1])

    scores: dict[str, float] = {}
    device = next(model.parameters()).device
    for label in labels:
        full_text = prompt_text + label
        encoded = tokenizer(full_text, return_tensors="pt", add_special_tokens=False)
        input_ids = encoded["input_ids"].to(device)
        if input_ids.shape[-1] <= prompt_length:
            scores[label] = -math.inf
            continue

        with torch.no_grad():
            logits = model(input_ids=input_ids).logits

        target_ids = input_ids[:, 1:]
        log_probs = F.log_softmax(logits[:, :-1, :], dim=-1)
        token_log_probs = log_probs.gather(-1, target_ids.unsqueeze(-1)).squeeze(-1)
        label_token_log_probs = token_log_probs[:, prompt_length - 1 :]
        scores[label] = float(label_token_log_probs.mean().detach().cpu())
    return scores


def build_metrics(predictions: list[dict[str, Any]], labels: list[str]) -> dict[str, Any]:
    from sklearn.metrics import classification_report, confusion_matrix

    y_true = [item["expected"] for item in predictions]
    y_pred = [item["predicted"] for item in predictions]
    report = classification_report(
        y_true,
        y_pred,
        labels=labels,
        output_dict=True,
        zero_division=0,
    )
    matrix = confusion_matrix(y_true, y_pred, labels=labels)
    correct = sum(1 for item in predictions if item["correct"])
    total = len(predictions)
    return {
        "summary": {
            "total": total,
            "correct": correct,
            "accuracy": 0.0 if total == 0 else round(correct / total, 4),
            "macro_f1": round(report.get("macro avg", {}).get("f1-score", 0.0), 4),
        },
        "labels": labels,
        "classification_report": report,
        "confusion_matrix": matrix.tolist(),
    }


def clean_prediction(text: str, labels: list[str]) -> str:
    """Kept for optional debug generation; label scoring is the default path."""
    text = re.sub(r"<think>.*?</think>", "", text or "", flags=re.DOTALL).strip()
    text = re.sub(r"<reasoning>.*?</reasoning>", "", text, flags=re.DOTALL).strip()
    text = re.sub(r"<\|.*?\|>", "", text).strip()
    lines = [line.strip() for line in text.splitlines() if line.strip()]
    text = lines[-1] if lines else ""
    if text in labels:
        return text
    for label in labels:
        if label in text:
            return label
    return "unknown"


def read_json(path: Path) -> Any:
    return json.loads(path.read_text(encoding="utf-8"))


def read_jsonl(path: Path) -> list[dict[str, Any]]:
    records = []
    with path.open("r", encoding="utf-8") as file:
        for line in file:
            if line.strip():
                records.append(json.loads(line))
    return records


def write_json(path: str | Path, payload: Any) -> None:
    Path(path).parent.mkdir(parents=True, exist_ok=True)
    Path(path).write_text(json.dumps(payload, ensure_ascii=False, indent=2), encoding="utf-8")


def write_jsonl(path: str | Path, records: list[dict[str, Any]]) -> None:
    Path(path).parent.mkdir(parents=True, exist_ok=True)
    with Path(path).open("w", encoding="utf-8") as file:
        for record in records:
            file.write(json.dumps(record, ensure_ascii=False) + "\n")


def write_confusion_matrix_csv(path: str | Path, metrics: dict[str, Any]) -> None:
    labels = metrics["labels"]
    matrix = metrics["confusion_matrix"]
    lines = ["," + ",".join(csv_escape(label) for label in labels)]
    for label, row in zip(labels, matrix):
        lines.append(csv_escape(label) + "," + ",".join(str(value) for value in row))
    Path(path).write_text("\n".join(lines) + "\n", encoding="utf-8")


def csv_escape(value: str) -> str:
    return '"' + value.replace('"', '""') + '"'


def safe_to_dict(value: Any) -> dict[str, Any]:
    if hasattr(value, "_asdict"):
        return dict(value._asdict())
    if hasattr(value, "__dict__"):
        payload = {}
        for key, item in vars(value).items():
            try:
                json.dumps(item)
                payload[key] = item
            except TypeError:
                payload[key] = str(item)
        return payload
    return {"value": str(value)}


