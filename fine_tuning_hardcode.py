"""Hardcoded Node 4-7 classification fine-tuning demo.

Purpose:
- Show the full post-dataset flow in one easy-to-read file.
- Run a classification-only QLoRA fine-tune on Vast.ai with Unsloth.
- Use Phase 1-3 artifacts already generated by this repo.
- Save adapter, tokenizer, metrics, predictions, and a compact run report.

Install on a fresh Vast.ai machine before running:
    pip install unsloth trl transformers datasets accelerate bitsandbytes scikit-learn pandas

Run:
    python fine_tuning_hardcode.py

This intentionally does not include OpenWebUI, Ollama, GGUF export, or OpenRouter.
"""

from __future__ import annotations

