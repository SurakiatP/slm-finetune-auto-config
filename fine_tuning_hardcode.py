# =============================================================================
# Hardcoded Node 4 config
# =============================================================================

RUN_ID = "demo-thai-classification-auto-001"
RUN_DIR = Path("runs") / RUN_ID
INPUT_DIR = RUN_DIR / "input"
OUTPUT_DIR = RUN_DIR / "fine_tuning_hardcode_qwen3_4b"
LOG_DIR = OUTPUT_DIR / "logs"

TRAIN_JSONL = INPUT_DIR / "train.jsonl"
VALIDATION_JSONL = INPUT_DIR / "validation.jsonl"
TEST_JSONL = INPUT_DIR / "test.jsonl"
PROMPT_TEMPLATE_JSON = INPUT_DIR / "classification_prompt_template.json"

MODEL_ID = "unsloth/Qwen3-4B"
MAX_SEQ_LENGTH = 2048
LOAD_IN_4BIT = True

LORA_RANK = 16
LORA_ALPHA = 16
LORA_DROPOUT = 0.05
LORA_TARGET_MODULES = [
    "q_proj",
    "k_proj",
    "v_proj",
    "o_proj",
    "gate_proj",
    "up_proj",
    "down_proj",
]

PER_DEVICE_TRAIN_BATCH_SIZE = 2
PER_DEVICE_EVAL_BATCH_SIZE = 2
GRADIENT_ACCUMULATION_STEPS = 4
LEARNING_RATE = 1e-4
NUM_TRAIN_EPOCHS = 1

# Vast.ai smoke setting: keep this finite for first verification.
# Set to None after the smoke run if you want a full epoch.
MAX_STEPS = 80

WARMUP_STEPS = 10
WEIGHT_DECAY = 0.05
LR_SCHEDULER_TYPE = "linear"
OPTIM = "adamw_8bit"
SEED = 3407
LOGGING_STEPS = 1
EVAL_STEPS = 25
SAVE_STEPS = 25

# Keep evaluation bounded for a first Vast smoke run.
# Set to None to evaluate all test records.
MAX_TEST_RECORDS = 100


def main() -> None:
    started_at = time.time()
    output_paths = make_output_dirs()
    validate_inputs()

    print("=== Node 4: hardcoded hyperparameter config ===")
    config = build_hardcoded_config()
    write_json(output_paths["config"], config)
    print(json.dumps(config, ensure_ascii=False, indent=2))

    print("\n=== Loading training libraries ===")
    FastLanguageModel, Dataset, SFTConfig, SFTTrainer, DataCollatorForSeq2Seq = import_training_libs()
    from unsloth.chat_templates import train_on_responses_only

    print("\n=== Loading model/tokenizer ===")
    model, tokenizer = FastLanguageModel.from_pretrained(
        model_name=MODEL_ID,
        max_seq_length=MAX_SEQ_LENGTH,
        dtype=None,
        load_in_4bit=LOAD_IN_4BIT,
    )
    model = FastLanguageModel.get_peft_model(
        model,
        r=LORA_RANK,
        target_modules=LORA_TARGET_MODULES,
        lora_alpha=LORA_ALPHA,
        lora_dropout=LORA_DROPOUT,
        bias="none",
        use_gradient_checkpointing="unsloth",
        random_state=SEED,
        use_rslora=False,
        loftq_config=None,
    )

    print("\n=== Loading Phase 1-3 SFT artifacts ===")
    prompt_artifact = read_json(PROMPT_TEMPLATE_JSON)
    labels = prompt_artifact["labels"]
    train_records = read_jsonl(TRAIN_JSONL)
    validation_records = read_jsonl(VALIDATION_JSONL)
    test_records = read_jsonl(TEST_JSONL)
    if MAX_TEST_RECORDS is not None:
        test_records = test_records[:MAX_TEST_RECORDS]
    print(
        f"records: train={len(train_records)}, validation={len(validation_records)}, "
        f"test_for_metrics={len(test_records)}"
    )
    print(f"labels: {labels}")

    train_dataset = Dataset.from_list(to_text_rows(train_records, tokenizer))
    validation_dataset = Dataset.from_list(to_text_rows(validation_records, tokenizer))

    print("\n=== Node 5: SLM fine-tuning ===")
    trainer = build_trainer(
        model=model,
        tokenizer=tokenizer,
        train_dataset=train_dataset,
        validation_dataset=validation_dataset,
        SFTConfig=SFTConfig,
        SFTTrainer=SFTTrainer,
        DataCollatorForSeq2Seq=DataCollatorForSeq2Seq,
    )
    trainer = train_on_responses_only(
        trainer,
        instruction_part="<|im_start|>user\n",
        response_part="<|im_start|>assistant\n",
    )
    train_result = trainer.train()
    write_json(output_paths["trainer_log_history"], trainer.state.log_history)
    write_json(output_paths["train_result"], safe_to_dict(train_result))

    print("\n=== Node 6: model inference + classification metrics ===")
    FastLanguageModel.for_inference(model)
    predictions = run_label_score_evaluation(
        model=model,
        tokenizer=tokenizer,
        records=test_records,
        labels=labels,
    )
    metrics = build_metrics(predictions, labels)
    write_json(output_paths["metrics"], metrics)
    write_jsonl(output_paths["predictions"], predictions)
    write_confusion_matrix_csv(output_paths["confusion_matrix"], metrics)
    print(json.dumps(metrics["summary"], ensure_ascii=False, indent=2))

    print("\n=== Node 7: export model adapter ===")
    model.save_pretrained(output_paths["adapter_dir"])
    tokenizer.save_pretrained(output_paths["adapter_dir"])
    report = {
        "run_id": RUN_ID,
        "model_id": MODEL_ID,
        "output_dir": output_paths["output_dir"],
        "adapter_dir": output_paths["adapter_dir"],
        "metrics_path": output_paths["metrics"],
        "predictions_path": output_paths["predictions"],
        "elapsed_seconds": round(time.time() - started_at, 2),
        "note": "PEFT adapter export only. No OpenWebUI/Ollama/GGUF export in this hardcoded demo.",
    }
    write_json(output_paths["report"], report)
    print(json.dumps(report, ensure_ascii=False, indent=2))


def run_with_logging() -> None:
    """Mirror console output to a timestamped log file for Vast.ai debugging."""
    LOG_DIR.mkdir(parents=True, exist_ok=True)
    log_path = LOG_DIR / f"fine_tuning_hardcode_{time.strftime('%Y%m%d_%H%M%S')}.log"
    original_stdout = sys.stdout
    original_stderr = sys.stderr
    with log_path.open("w", encoding="utf-8", buffering=1) as log_file:
        sys.stdout = TeeStream(original_stdout, log_file)
        sys.stderr = TeeStream(original_stderr, log_file)
        try:
            print(f"Logging to: {log_path.as_posix()}")
            print_runtime_summary()
            main()
            print(f"\nSUCCESS. Full log saved to: {log_path.as_posix()}")
        except BaseException:
            print("\nFAILED. Full traceback:")
            traceback.print_exc()
            print(f"\nFull log saved to: {log_path.as_posix()}")
            raise
        finally:
            sys.stdout = original_stdout
            sys.stderr = original_stderr


class TeeStream:
    """Write stream output to both console and a log file."""

    def __init__(self, *streams):
        self.streams = streams

    def write(self, data: str) -> int:
        for stream in self.streams:
            stream.write(data)
            stream.flush()
        return len(data)

    def flush(self) -> None:
        for stream in self.streams:
            stream.flush()

    @property
    def encoding(self) -> str:
        return "utf-8"


